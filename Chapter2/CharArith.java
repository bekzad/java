
public class CharArith {
    public static void main(String args[]) {
        char ch = 'X';
        System.out.println("Ch is: " + ch);
        ch++;
        System.out.println("Incremented ch is: " + ch);
        ch = 90;
        System.out.println("Char assigned integer value 90 is: " + ch);
    }
}