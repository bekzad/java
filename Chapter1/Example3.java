class Example3 {
    public static void main(String args[]) {
        int x = 10;
        double y = 10.0;
        System.out.println("The original value of x: " + x);
        System.out.println("The original value of y: " + y);
        System.out.println();

        x = x / 4;
        y = y / 4;

        System.out.println("The value of x after division by 4: " + x);
        System.out.println("The value of y after division by 4: " + y);
    }
}